vim.g.mapleader = ' '

vim.opt.termguicolors = true

-- Relative number always active
vim.opt.number = true        -- show absolute number on current line
vim.opt.relativenumber = true

vim.opt.cursorline = true

-- Keep 8 lines visible above/below cursor
vim.opt.scrolloff = 8

-- Indentation size 2
vim.opt.tabstop = 2          -- how many spaces a tab counts for
vim.opt.shiftwidth = 2       -- size of an indent
vim.opt.softtabstop = 2      -- spaces when pressing <Tab>
vim.opt.expandtab = true     -- use spaces instead of tabs

vim.opt.wrap = false
